income = float(input("กรอกรายได้ต่อเดือนของคุณ (บาท): "))

if income < 15000:
    print("ไม่สามารถสมัครบัตรเครดิตได้ เนื่องจากรายได้ต่ำกว่าเกณฑ์")
elif income < 70000:
    print("คุณสามารถสมัครบัตรเครดิต Silver ได้")
elif income < 100000:
    print("คุณสามารถสมัครบัตรเครดิต Gold ได้")
else:
    print("คุณสามารถสมัครบัตรเครดิต Platinum ได้")
